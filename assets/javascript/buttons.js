$(document).ready(function(){
    $(".sportsContainer").click(function(){
        $(".sportsContainer").hide();
        $(".musicContainer").hide();
        $(".familyContainer").hide();
        $(".sportsContainerLARGE").show();
    });
    $(".musicContainer").click(function(){
        $(".musicContainer").hide();
        $(".sportsContainer").hide();
        $(".familyContainer").hide();
        $(".musicContainerLARGE").show();
    });
    $(".familyContainer").click(function(){
        $(".familyContainer").hide();
        $(".sportsContainer").hide();
        $(".musicContainer").hide();
        $(".familyContainerLARGE").show();
    });
    $(".sportsContainerLARGE").click(function(){
        $(".sportsContainer").show();
        $(".musicContainer").show();
        $(".familyContainer").show();
        $(".sportsContainerLARGE").hide();
    });
    $(".musicContainerLARGE").click(function(){
        $(".sportsContainer").show();
        $(".musicContainer").show();
        $(".familyContainer").show();
        $(".musicContainerLARGE").hide();
    });
    $(".familyContainerLARGE").click(function(){
        $(".sportsContainer").show();
        $(".musicContainer").show();
        $(".familyContainer").show();
        $(".familyContainerLARGE").hide();
    });
});
